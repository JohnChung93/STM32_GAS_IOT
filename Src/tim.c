/**
  * @brief  Period elapsed callback in non blocking mode 
  * @param  htim : TIM handle
  * @retval None
  */
void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim)
{
  /* Prevent unused argument(s) compilation warning */
    if(htim==(&htim1))
    {
			if(Timer_Ptr_Count(&GAS_Detect_Count,Gas_Detect_Time_S))
			{
				GAS_DETECT_FLAG = 1;
			}
       
			if(ESP8266_STATUS == ESP8266_STATE_READY)
			{
				if(Timer_Ptr_Count(&Upload_Count,UPLOAD_TIME_S))
				{
					UPLOAD_FLAG = 1;
				}
			}

    }
  /* NOTE : This function Should not be modified, when the callback is needed,
            the __HAL_TIM_PeriodElapsedCallback could be implemented in the user file
   */

}

void Set_Upload_Count(uint8_t count){
	Upload_Count = count;
}

uint8_t Timer_Ptr_Count(uint8_t* count,uint8_t target){
	if(*count == target)
	{
		*count= 0;
		return 1;
	}
	else
	{
		*count = *count + 1;
		return 0;
	}
}
