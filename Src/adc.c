/**
  * @brief  Get ADC value
  * @param  ch: adc channel 0~16ï¼ŒChannel value range:ADC_CHANNEL_0~ADC_CHANNEL_16
  * @retval ADC translate result
  */
uint16_t Get_Adc(uint32_t ch)   
{
    ADC_ChannelConfTypeDef ADC1_ChanConf;
    
    ADC1_ChanConf.Channel=ch;                                    //channel
    ADC1_ChanConf.Rank=1;                                        //Get channel 1ï¼?
    ADC1_ChanConf.SamplingTime=ADC_SAMPLETIME_239CYCLES_5;       //Sampling time              
    HAL_ADC_ConfigChannel(&hadc1,&ADC1_ChanConf);       				 //Set channel
	
    HAL_ADC_Start(&hadc1);                               				 //ADC start
	
    HAL_ADC_PollForConversion(&hadc1,10);               				 //Poll conversion
 
	return (uint16_t)HAL_ADC_GetValue(&hadc1);	        					 //Return latest ADC1 translate result value
}

/**
  * @brief  Get the converted value of the specified channelï¼Œthen average wiht times
  * @param  ch: adc channel 0~16ï¼ŒChannel value range:ADC_CHANNEL_0~ADC_CHANNEL_16
						times: average times
  * @retval Channel ch average wiht times
  */
uint16_t Get_Adc_Average(uint8_t ch,uint8_t times)
{
	uint32_t temp_val=0;
	uint8_t t;
	for(t=0;t<times;t++)
	{
		temp_val+=Get_Adc(ch);
		HAL_Delay(5);
	}
	return temp_val/times;
} 

